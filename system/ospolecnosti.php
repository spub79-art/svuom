<h2>History of company</h2>

<p>In its scope of activities, SVÚOM Company Limited is the successor of SVÚOM
State Research Institute for Materials Protection in Prague.</p>

<br />

<h3>Period 1949 - 1999</h3>

<p>SVÚOM was founded in 1949 by Milič Roubal, who pursued the idea of combining, within
a single organization, the research into the corrosion of metals with that relating to the
problems of metal finishing — an approach which was unique at the time, worldwide. In
those days there were established research organizations dealing with metals corrosion,
and other research organizations focused on surface finishing.</p>

<p>The new institute took up the results
achieved by Czech scientists of the period,
including V. Čupr, J. Teindl, J. Rožan and
some others who had studied various facets
of the corrosion protection of metals. The
major areas of knowledge required for this
rather broad field of science were covered
by sourcing the works of the ''forefathers'' of
corrosion protection, such as U.R.Evans,
M.Pourbaix, G.V.Akimov, M.G.Fontana, P.
Lacombe, J.E.O.Mayne, W.J. Müller, H. H.
Uhlig, C. Wagner and many others.</p>

<p class='picalignr'><img src='images/historie/1.jpg' title='Historie společnosti'></p>

<p>The problems relating to the protection of
metals from corrosion attack came to be part
of the curricula since the 1950s at the Prague
Institute of Chemistry & Technology, at
Mining School at Ostrava and at the
Pardubice Institute of Chemical Technology,
and in subsequent years, elsewhere at
Czechoslovak universities. Since the
early 1960s, Czech translations of
foreign monographs as well as original
Czech book productions started
appearing on the local market.</p>

<p>Applied research was focused on the
corrosion of metals, surface finishings,
and dealing with problems where the
corrosion damage caused to electrical
and electrotechnology devices was
affected by environmental factors.
SVÚOM acted as a comprehensive
research center addressing to
problems relating to the degradation
and protection of products made of
metals and exposed to various
atmospheric and service conditions.</p>

<br />

<h3>Period 1999 - 2017</h3>

<p>The current research program is focused
on advancing our understanding of the
new mechanisms underlying the corrosion
behavior of materials and coatings in a
variety of industries and in all modes of
transport. New experimental techniques
are being tested, and data is being
collected on new materials and coatings
under prototype testing conditions, hand in
hand with the development of progressive
methods also involving models capable of
forecasting certain types of corrosion.</p>

<p class='picalignl'><img src='images/historie/2.jpg' title='Historie společnosti'></p>

<p>SVÚOM´s specialists are lectors in various
