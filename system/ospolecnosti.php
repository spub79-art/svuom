<h2>About the company</h2>
<div class='rightpl'><img src='images/historyhome.jpg'></div>
<div class='justtext'>
SVÚOM Ltd. is a company, which specialises in the field of corrosion, protection against corrosion and surface treatment. It continues and follows up the expert activities of the National Research Institute for the Protection of Materials (SVÚOM). This state institute was established in 1954 and its main intention was to provide a complex and centralized research and development of protection against corrosion in Czechoslovakia. During its nearly 50 years of existence this department gained a noted and significant position in the field of corrosion and material protection. The state research department for the protection of materials was the resort research institute of Ministry of Industry. After the completion of an expert activity of the State Research Institute for the Protection of Materials, the valuable work experience gained over many years of practice is therefore further used and developed by SVÚOM Ltd. in the field of corrosion and corrosion protection according to customer requirements.</div>
