	<div class='highslide-maincontent'>
	Po vice než 150 let je vytvoření povlaků zinku ponorem do taveniny standardním způsobem protikorozní ochrany oceli.

Běžným omylem je představa, že žárový povlak zinku je relativně snadné definovat. Ačkoliv se žárové zinkování používá pro ochranu oceli po generace, přináší proces zinkování mnoho problémů. Při žárovém pokovení ponorem do taveniny zinku (450–470 °C) narůstá na zinkovaných dílech slitinový železo-zinkový povlak, který je výsledkem poměrně složitého procesu vzájemné difuze atomů obou kovů za vniku elementárních intermetalických vazeb a následných fázových přeměn. Tyto děje probíhají jednak v povrchové vrstvě zinkovaného kovu, dále na rozhraní mezi povrchem kovu a taveninou zinku a konečně i v samotné tavenině v blízkosti zinkovaného dílu. Chemické složení a stav povrchu (úprava a drsnost) podkladového kovu, hmotnost součástí a podmínky zinkování mohou ovlivnit vzhled, tloušťku, strukturu a fyzikální/mechanické vlastnosti povlaku.

Cílem projektu je získat informace o vlivu chemického složení, metalurgického stavu povrchu oceli, vlivu různých technologií zpracování oceli a dalších faktorů vlastního procesu zinkování ovlivňujících kvalitu žárového zinkového povlaku. Přesto, že jsou v technických normách uváděny třídy ocelí vhodných k žárovému zinkování, je zinkování rozměrných ocelových konstrukcí stále problematická záležitost a v řadě případů vznikají povlaky s nízkou přilnavostí, což se následně projevuje v jejich vyšším poškození při montáži konstrukcí, nutnosti dodatečné protikorozní ochrany, apod. Především stožáry VN jsou typem výrobků, kde je požadována vysoká kvalita zinkového povlaku jak z hlediska jeho tloušťky, která zajišťuje dlouhodobou protikorozní ochranu, tak i z hlediska přilnavosti povlaku.

Dosavadní spolupráce na řešení problémů s nízkou přilnavostí žárových zinkových povlaků ukazuje, že je nutné vytvořit systematickou databázi údajů o chemickém složení podkladových ocelí (křemíkem uklidněné oceli, hliníkem uklidněné oceli, distribuce Si a dalších prvků v povrchových vrstvách ocelí), jejich metalurgickém stavu (válcované za tepla, za studena, mikrotvrdost povrchu, stav zrna oceli, apod.), vlivu opracování ocelových dílů (broušení, vrtání, řezání, apod.), podmínkách jednotlivých kroků v procesu žárového zinkování (parametry moření, teplota a doba zinkování, složení zinkovací lázně, popř. přídavky legujících prvků, kinetiky chladnutí po zinkování) ve vztahu k vytvořenému povlaku - jeho tloušťce, struktuře a přilnavosti.

